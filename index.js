// !== (" не дорівнює, not equal")
// Бінарий оператор, повертає boolean
// Перевіряє, чи обидва операнди не рівні одне одному
// якщо операнди не рівні між собою - повертає true
// якщо операнди рівні між собою - повертає false

// задача. дано число. перевірити чи це число рівне 5

const number = 10

if (number !== 5) {
    console.log( number + ',Число не дорівнює 5');
} else {
    console.log('Число дорівнює 5');
}